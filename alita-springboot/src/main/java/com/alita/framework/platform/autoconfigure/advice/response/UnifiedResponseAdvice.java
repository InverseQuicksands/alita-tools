package com.alita.framework.platform.autoconfigure.advice.response;

import org.springframework.core.MethodParameter;
import org.springframework.http.MediaType;
import org.springframework.http.converter.HttpMessageConverter;
import org.springframework.http.converter.StringHttpMessageConverter;
import org.springframework.http.server.ServerHttpRequest;
import org.springframework.http.server.ServerHttpResponse;
import org.springframework.web.bind.annotation.RestControllerAdvice;
import org.springframework.web.servlet.mvc.method.annotation.ResponseBodyAdvice;

/**
 * 全局响应信息处理.
 * <p> 实现 {@code ResponseBodyAdvice} 接口，拦截Controller方法默认返回参数，统一处理返回值/响应体.
 *
 * @author Zhang Liang
 * @date 2021/2/9
 * @since 1.0
 */
@RestControllerAdvice
public class UnifiedResponseAdvice implements ResponseBodyAdvice<Object>{

    /**
     * Whether this component supports the given controller method return type
     * and the selected {@code HttpMessageConverter} type.
     *
     * @param returnType    the return type
     * @param converterType the selected converter type
     * @return {@code true} if {@link #beforeBodyWrite} should be invoked;
     * {@code false} otherwise
     */
    @Override
    public boolean supports(MethodParameter returnType, Class<? extends HttpMessageConverter<?>> converterType) {
        boolean stringAssignable = StringHttpMessageConverter.class.isAssignableFrom(converterType);
        boolean responseAssignableFrom = Response.class.isAssignableFrom(returnType.getMethod().getReturnType());

        if (stringAssignable || responseAssignableFrom) {
            return false;
        }

        return true;
    }

    @Override
    public Object beforeBodyWrite(Object body, MethodParameter returnType, MediaType selectedContentType,
                                  Class<? extends HttpMessageConverter<?>> selectedConverterType,
                                  ServerHttpRequest request, ServerHttpResponse response) {
        return Response.success(body);
    }



}
